/**
 * 0.96 寸 OLED 显示屏驱动（SSD1306 芯片，I2C 接口）
 *
 * 这是整个项目里最复杂的驱动，但核心就两件事：
 *   1. 会发 I2C 起始/停止信号
 *   2. 会往 SSD1306 的显存里写数据
 *
 * 硬件：
 *   - 屏幕：0.96 寸 128x64 OLED
 *   - 芯片：SSD1306（屏幕背后的小芯片，负责驱动 OLED 像素）
 *   - 接口：I2C（PB6=SCL, PB7=SDA）
 *   - 地址：0x3C（7位地址），左移一位 = 0x78（8位地址）
 *
 * SSD1306 的显存结构：
 *   128 列 × 64 行 = 8192 个像素
 *   但 SSD1306 按"页"管理：64 行 ÷ 8 = 8 页
 *   每页 128 列，每列 8 个像素（一个字节，每一位代表一个像素）
 *
 *   页0: 第 0~7 行
 *   页1: 第 8~15 行
 *   ...
 *   页7: 第 56~63 行
 *
 *   想点亮某个像素？找到它在哪一页哪一列，把对应的 bit 置 1。
 */

#include "oled.h"
#include "delay.h"

/* ========== I2C 硬件引脚定义 ========== */
#define OLED_SCL_PORT       GPIOB
#define OLED_SCL_PIN        GPIO_Pin_6
#define OLED_SDA_PORT       GPIOB
#define OLED_SDA_PIN        GPIO_Pin_7
#define OLED_GPIO_RCC       RCC_AHB1Periph_GPIOB

/* OLED 的 I2C 地址（8位格式，包含读写位） */
#define OLED_ADDRESS        0x78

/* SSD1306 命令/数据控制字节 */
#define OLED_CMD            0x00    /* 后面跟的是命令 */
#define OLED_DATA           0x40    /* 后面跟的是数据 */

/* ========== 软件 I2C 底层函数 ==========
 * 这里用"软件模拟 I2C"（bit-bang），而不是 STM32 的硬件 I2C 外设。
 * 为什么？因为：
 *   1. 软件 I2C 代码更直观，你能看到每一个时钟周期发生了什么
 *   2. 硬件 I2C 配置复杂，STM32 的 I2C 还有已知的硬件 bug（勘误表）
 *   3. OLED 通信速度要求不高，软件模拟完全够用
 *
 * 你以后学到硬件 I2C 时，可以把这些函数换成 HAL 库的 I2C 函数。
 */

/**
 * I2C_Start - 发起 I2C 起始信号
 *
 * 起始信号的定义：
 *   SCL 保持高电平期间，SDA 从高变低。
 *
 *   正常时序中，SDA 只在 SCL 低电平时才能变化。
 *   SCL 高电平时 SDA 变化，这就是特殊的"起始/停止"信号。
 *
 *   SCL: ‾‾‾‾‾‾‾‾‾‾
 *   SDA: ‾‾‾‾╲______
 *            ↑ 这个下降沿就是起始信号
 */
static void I2C_Start(void)
{
    /* 确保 SDA 和 SCL 都是高电平 */
    GPIO_SetBits(OLED_SDA_PORT, OLED_SDA_PIN);
    GPIO_SetBits(OLED_SCL_PORT, OLED_SCL_PIN);
    Delay_us(2);

    /* SCL 为高时，SDA 拉低 → 起始信号 */
    GPIO_ResetBits(OLED_SDA_PORT, OLED_SDA_PIN);
    Delay_us(2);

    /* 拉低 SCL，开始传数据 */
    GPIO_ResetBits(OLED_SCL_PORT, OLED_SCL_PIN);
    Delay_us(2);
}

/**
 * I2C_Stop - 发送 I2C 停止信号
 *
 * 停止信号的定义：
 *   SCL 保持高电平期间，SDA 从低变高。
 *
 *   SCL: ‾‾‾‾‾‾‾‾‾‾
 *   SDA: ______╱‾‾‾‾
 *            ↑ 这个上升沿就是停止信号
 */
static void I2C_Stop(void)
{
    /* SDA 先拉低 */
    GPIO_ResetBits(OLED_SDA_PORT, OLED_SDA_PIN);
    Delay_us(2);

    /* SCL 拉高 */
    GPIO_SetBits(OLED_SCL_PORT, OLED_SCL_PIN);
    Delay_us(2);

    /* SCL 为高时，SDA 拉高 → 停止信号 */
    GPIO_SetBits(OLED_SDA_PORT, OLED_SDA_PIN);
    Delay_us(2);
}

/**
 * I2C_WaitAck - 等待从机应答
 *
 * 发完一个字节后，从机（OLED）会拉低 SDA 表示"收到了"（ACK）。
 * 如果 SDA 还是高电平，说明从机没响应（NACK）。
 *
 * 实际上 SSD1306 很少 NACK，但检查一下是好习惯。
 *
 * 返回值：
 *   0 = ACK（正常）
 *   1 = NACK（出错）
 */
static uint8_t I2C_WaitAck(void)
{
    uint8_t timeout = 0;

    /* SDA 设为输入模式（释放总线，让从机控制） */
    GPIO_SetBits(OLED_SDA_PORT, OLED_SDA_PIN);  /* 先拉高，准备读 */
    Delay_us(1);

    /* SCL 拉高，让从机有机会把 SDA 拉低 */
    GPIO_SetBits(OLED_SCL_PORT, OLED_SCL_PIN);
    Delay_us(1);

    /* 读 SDA 的状态 */
    while (GPIO_ReadInputDataBit(OLED_SDA_PORT, OLED_SDA_PIN)) {
        timeout++;
        if (timeout > 250) {
            I2C_Stop();  /* 超时，发停止信号退出 */
            return 1;    /* NACK */
        }
    }

    /* SCL 拉低，完成这一位的时钟 */
    GPIO_ResetBits(OLED_SCL_PORT, OLED_SCL_PIN);
    Delay_us(2);
    return 0;  /* ACK */
}

/**
 * I2C_WriteByte - 发送一个字节
 *
 * I2C 传输规则：
 *   - SCL 低电平时，主机可以改变 SDA 的值（准备数据）
 *   - SCL 高电平时，从机读取 SDA 的值（采样数据）
 *   - 一个字节 8 位，从高位（MSB）开始发
 *
 * 举例：发送 0xA5 = 10100101
 *   第1轮：SCL低 → SDA=1 → SCL高 → 从机读到1 → SCL低
 *   第2轮：SCL低 → SDA=0 → SCL高 → 从机读到0 → SCL低
 *   第3轮：SCL低 → SDA=1 → SCL高 → 从机读到1 → SCL低
 *   ...以此类推，共 8 轮
 */
static void I2C_WriteByte(uint8_t data)
{
    uint8_t i;

    for (i = 0; i < 8; i++) {
        /* SCL 低电平时准备数据 */
        GPIO_ResetBits(OLED_SCL_PORT, OLED_SCL_PIN);
        Delay_us(1);

        /* 取出最高位，放到 SDA 上 */
        if (data & 0x80) {
            GPIO_SetBits(OLED_SDA_PORT, OLED_SDA_PIN);
        } else {
            GPIO_ResetBits(OLED_SDA_PORT, OLED_SDA_PIN);
        }
        data <<= 1;  /* 左移一位，下一位变成最高位 */

        /* SCL 高电平，让从机采样 */
        GPIO_SetBits(OLED_SCL_PORT, OLED_SCL_PIN);
        Delay_us(2);

        /* SCL 拉低，准备下一位 */
        GPIO_ResetBits(OLED_SCL_PORT, OLED_SCL_PIN);
        Delay_us(1);
    }

    /* 等待从机应答 */
    I2C_WaitAck();
}

/* ========== SSD1306 命令/数据发送 ========== */

/**
 * OLED_WriteCmd - 向 SSD1306 发送命令
 *
 * I2C 格式：[起始] [地址0x78] [控制字节0x00] [命令字节] [停止]
 *           0x00 表示后面跟的是"命令"
 */
static void OLED_WriteCmd(uint8_t cmd)
{
    I2C_Start();
    I2C_WriteByte(OLED_ADDRESS);   /* 设备地址 */
    I2C_WriteByte(OLED_CMD);       /* 控制字节：命令 */
    I2C_WriteByte(cmd);            /* 实际命令 */
    I2C_Stop();
}

/**
 * OLED_WriteData - 向 SSD1306 发送数据（像素内容）
 *
 * I2C 格式：[起始] [地址0x78] [控制字节0x40] [数据字节] [停止]
 *           0x40 表示后面跟的是"显存数据"
 */
static void OLED_WriteData(uint8_t data)
{
    I2C_Start();
    I2C_WriteByte(OLED_ADDRESS);   /* 设备地址 */
    I2C_WriteByte(OLED_DATA);      /* 控制字节：数据 */
    I2C_WriteByte(data);           /* 实际像素数据 */
    I2C_Stop();
}

